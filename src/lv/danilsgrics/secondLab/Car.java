package lv.danilsgrics.secondLab;

public class Car {
    String colourOfCar;
    String makeOfCar;
    int productionDateOfCar;

    public String getColourOfCar() {
        return colourOfCar;
    }

    public String getMakeOfCar() {
        return makeOfCar;
    }

    public int getProductionDateOfCar() {
        return productionDateOfCar;
    }

    public void setColourOfCar(String colourOfCar) {
        this.colourOfCar = colourOfCar;
    }

    public void setMakeOfCar(String makeOfCar) {
        this.makeOfCar = makeOfCar;
    }

    public void setProductionDateOfCar(int productionDateOfCar) {
        this.productionDateOfCar = productionDateOfCar;
    }
}
